    public void showTable() {
    EntityManagerFactory emf = Persistence.createEntityManagerFactory("SimulasiUASPU");
    EntityManager em = emf.createEntityManager();
    try {
        DefaultTableModel tbnmhs = new DefaultTableModel(new String[]{"NIM", "NAMA", "ALAMAT", "ASAL SMA", "NAMA ORTU"}, 0);
        List<Mahasiswa> mhsList = em.createNamedQuery("Mahasiswa.findAll", Mahasiswa.class).getResultList();
        for (Mahasiswa mhs : mhsList) {
            tbnmhs.addRow(new Object[]{
                mhs.getNim(),
                mhs.getNama(),
                mhs.getAlamat(),
                mhs.getAsalsma(),
                mhs.getNamaortu()
            });
        }
        tblMhs.setModel(tbnmhs);
    } catch (Exception ex) {
        Logger.getLogger(DataMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
    } finally {
        em.close();  
    }
}

