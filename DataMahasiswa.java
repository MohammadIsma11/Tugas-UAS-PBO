    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser();
        int returnValue = jfc.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File file = jfc.getSelectedFile();
            System.out.println("File yang dipilih: " + file.getAbsolutePath());
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("SimulasiUASPU");
            EntityManager em = emf.createEntityManager();
            try (BufferedReader br = new BufferedReader(new FileReader(file))) {
                String line;
                em.getTransaction().begin();
                while ((line = br.readLine()) != null) {
                    String[] data = line.split(";");
                    if (data.length >= 4) {
                        String n = data[0];
                        String na = data[1];
                        String a = data[2];
                        String as = data[3];
                        String no = data[4];
                        showTable();
                        if (!n.isEmpty() && !na.isEmpty() && !a.isEmpty() && !as.isEmpty() && !no.isEmpty()) {
                            Mahasiswa mhs = new Mahasiswa();
                            mhs.setNim(n);
                            mhs.setNama(na);
                            mhs.setAlamat(a);
                            mhs.setAsalsma(as);
                            mhs.setNamaortu(no);
                            em.persist(mhs);
                        }
                    }
                }
                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Data berhasil diupload");
                showTable();
            } catch (IOException | PersistenceException e) {
                if (em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
                JOptionPane.showMessageDialog(null, "Gagal mengupload data");
                Logger.getLogger(DataMahasiswa.class.getName()).log(Level.SEVERE, null, e);
            } finally {
                if (em.isOpen()) {
                    em.close();
                }
                emf.close();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

